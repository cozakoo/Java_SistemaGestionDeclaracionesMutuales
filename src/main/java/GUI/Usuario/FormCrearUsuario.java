    private boolean NombreUsuarioExiste(String userName) {

        try {
            return DataBase.getInstance(true).consulta("SELECT * FROM usuario WHERE username = '" + userName + "'").next();
        } catch (SQLException ex) {
            Logger.getLogger(FormCrearUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;

    }

