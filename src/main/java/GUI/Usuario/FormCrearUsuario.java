    private void CrearUsuarioButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CrearUsuarioButtonMouseClicked
        if (!camposVacios()) {
            Boolean Verificacion = true;
            if (!contraseñaSonIgules()) {
                AdvertenciaContraseñ.setVisible(true);
                Verificacion = false;
            } else {
                AdvertenciaContraseñ.setVisible(false);
            }

            if (NombreUsuarioExiste(NombrejTextField.getText().toString())) {
                AdvertenciaUsuario.setVisible(true);
                Verificacion = false;
            } else {
                AdvertenciaUsuario.setVisible(false);
            }

            if (Verificacion) {
                AdvertenciaUsuario.setVisible(false);
                AdvertenciaContraseñ.setVisible(false);
                String nombre = NombrejTextField.getText().toString();
                String contrasenia = jPasswordField.getText().toString();
                Usuario user ;
                if (IsAdminjCheckBox.isSelected()) {
                     user = new Usuario(nombre, contrasenia, RolUsuario.administrador);
                } else {
                     user = new Usuario(nombre, contrasenia);
                }
                user.insertarEnBaseDeDatos();
                limpiarCampos();
                update.updateTableData();

            }
        } else {
            Utilidades.mensajeAdvertencia("Complete los campos.");
        }
    }

    private boolean contraseñaSonIgules() {
        return jPasswordField.getText().toString().equals(confirmacionjPasswordField.getText().toString());

    }//GEN-LAST:event_CrearUsuarioButtonMouseClicked

