    private void cargarImagenes() {
        String dirActual = System.getProperty("user.dir");
        ModificarButton.setIcon(new javax.swing.ImageIcon(dirActual + url_imagen_rigth));
        NombrejTextField.setText(this.mutual.getNombre());
        try {
            BufferedImage iconImage = ImageIO.read(new File(dirActual + "\\images\\icono.png"));

            // Establecer la imagen como ícono de la aplicación
            this.setIconImage(iconImage);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
