    private void ModificarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ModificarButtonMouseClicked

        if (!NombrejTextField.getText().isEmpty()) {

            try {

                this.mutual.setNombre(NombrejTextField.getText());

                if (!this.mutual.existeMutual()) {
                    if (Utilidades.mensajeConfirmar("¿Desea confirmar la modificación?", "Confirmar modificación")) {

                        this.mutual.modificarMutualActiva(NombrejTextField.getText());
                        update.updateTableData();
                        this.dispose();
                        Utilidades.mensajeExito("Mutual modificada con exito!.");
                    }
                } else {
                    Utilidades.mensajeError("Ya existe otra mutual con ese nombre.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ModificarMutual.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            Utilidades.mensajeAdvertencia("El nombre no puede estar vacío.");
        }
    }//GEN-LAST:event_ModificarButtonMouseClicked

